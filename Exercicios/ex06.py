tabuada = int(input('Insira um número para saber sua tabuada: '))

vezesUm = tabuada * 1 
vezesDois = tabuada * 2
vezesTres = tabuada * 3
vezesQuatro = tabuada * 4
vezesCinco = tabuada * 5
vezesSeis = tabuada * 6
vezesSete = tabuada * 7
vezesOito = tabuada * 8
vezesNove = tabuada * 9
vezesDez = tabuada * 10

print('A tabuada desse número {} é\n {}\n {}\n {}\n {}\n {}\n {}\n {}\n {}\n {}\n {}\n'.format(tabuada, vezesUm, vezesDois, vezesTres, vezesQuatro, vezesCinco, vezesSeis, vezesSete, vezesOito, vezesNove, vezesDez))